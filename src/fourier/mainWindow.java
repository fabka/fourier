    private void button_goActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_goActionPerformed

        int n = Integer.parseInt(textField_n.getText());
        double tMax = Double.parseDouble(textField_tMax.getText());
        ArrayList<Integer> gt = new ArrayList<>();
        String tf = textField_gt.getText();
        for (int i = 0; i <tf.length() ; i++) {
            if ( textField_gt.getText().charAt(i) == '1' )
                gt.add(1);
            else
                gt.add(0);
        }
        
        int BPS = Integer.parseInt(textField_BPS.getText());
        int f = BPS/tf.length();
        jLabel_frecuencia.setText(f+" Hz");
        double detalle = Double.parseDouble(jTextField_detalle.getText());
        double t = 0;
        t *= (double) 1000; 
        int ventana = (int) t+1;

        fourier = new Fourier(f, gt, n, tMax*0.001, detalle*0.000001);
        fourier.crearSerieFourier();
        JOptionPane.showMessageDialog(this, "SimulaciÃ³n finalizada");

    }//GEN-LAST:event_button_goActionPerformed

